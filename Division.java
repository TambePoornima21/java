class Division 
{
	public static void main(String[] args) 
	{
		byte b1=10;
		byte b2=2;
		int b3=b1/b2;
		System.out.println(b3);

		short s1=129;
		short s2=145;
		int s3=s1/s2;
		System.out.println(s3);

		int i1=2345;
		int i2=2435;
		int i3=i1/i2;
		System.out.println(i3);

		long l1=300l;
		long l2=245l;
		long l3=l1/l2;
		System.out.println(l3);

		float f1=2.34f;
		float f2=3.45f;
		float f3=f1/f2;
		System.out.println(f3);

	}
}
